// Generate a branded Digit2AI "Project Qualification Brief" PDF from a
// project's triage_structured output. Streams the PDF directly into an
// Express response — caller is the route handler.
//
// Why pdfkit (not puppeteer): pure Node, no headless Chrome to install,
// no Render cold-start penalty, deterministic output, ~100 KB per PDF.

const PDFDocument = require('pdfkit');
const { fetchWithTimeout } = require('./agents/fetchWithTimeout');

// Digit2AI brand colors (hex)
const COLOR_INK     = '#0f172a';
const COLOR_MUTED   = '#64748b';
const COLOR_ACCENT  = '#7c5cff'; // purple-blue gradient endpoint
const COLOR_ACCENT2 = '#22d3ee'; // cyan gradient endpoint
const COLOR_GO      = '#10b981';
const COLOR_WARN    = '#f59e0b';
const COLOR_STOP    = '#ef4444';

const LOGO_URL = 'https://assets.cdn.filesafe.space/3lSeAHXNU9t09Hhp9oai/media/69d97bc215a505b6793950c0.png';

// Cache the logo across requests so we don't refetch on every PDF.
// Cap at 200KB — bigger and we skip the embed and use a text header
// instead, otherwise every PDF balloons to 2+ MB which is awful for
// WhatsApp recipients on metered data.
const LOGO_MAX_BYTES = 200 * 1024;
let _logoBuf = null;
let _logoChecked = false;
async function getLogoBuffer() {
  if (_logoChecked) return _logoBuf;
  _logoChecked = true;
  try {
    const resp = await fetch(LOGO_URL);
    if (!resp.ok) return null;
    const ab = await resp.arrayBuffer();
    if (ab.byteLength > LOGO_MAX_BYTES) {
      console.warn(`[triagePdf] logo too large (${ab.byteLength} bytes) — using text header instead`);
      _logoBuf = null;
      return null;
    }
    _logoBuf = Buffer.from(ab);
    return _logoBuf;
  } catch (_) { return null; }
}

